import os
import time
import requests
import FinanceDataReader as fdr

# ==================================================
# 디스코드 웹훅
# ==================================================
WEBHOOK_URL = os.getenv("WEBHOOK_URL")

# ==================================================
# 디스코드 전송
# ==================================================
def send_discord_message(message):

    try:

        requests.post(
            WEBHOOK_URL,
            json={
                "content": message
            },
            timeout=10
        )

    except Exception as e:

        print(e)

# ==================================================
# 메인
# ==================================================
def main():

    print("🚀 테스트 시작")

    # ==================================================
    # 종목 리스트
    # ==================================================
    stocks = fdr.StockListing('KRX')

    stocks = stocks[
        stocks['Market'].isin([
            'KOSPI',
            'KOSDAQ'
        ])
    ]

    # ==================================================
    # 결과 저장
    # ==================================================
    results = []

    # ==================================================
    # 테스트용 50개만
    # ==================================================
    stocks = stocks.head(50)

    for _, row in stocks.iterrows():

        try:

            code = row['Code']
            name = row['Name']

            print(name)

            # ==================================================
            # 데이터 가져오기
            # ==================================================
            data = fdr.DataReader(
                code,
                '2025-01-01'
            )

            data = data.dropna()

            if len(data) < 5:

                print(name, "데이터 부족")

                continue

            # ==================================================
            # 최근 데이터
            # ==================================================
            today = data.iloc[-1]

            close = today['Close']
            volume = today['Volume']

            # ==================================================
            # 거래대금
            # ==================================================
            trading_value = (
                close
                * volume
            )

            # ==================================================
            # 저장
            # ==================================================
            results.append({

                "name": name,

                "close": int(close),

                "trading_value": int(
                    trading_value
                    / 100000000
                )
            })

        except Exception as e:

            print(name, e)

    # ==================================================
    # 거래대금 정렬
    # ==================================================
    results = sorted(

        results,
        key=lambda x: x['trading_value'],
        reverse=True

    )

    top_results = results[:5]

    # ==================================================
    # 결과 확인
    # ==================================================
    print("최종 개수:", len(top_results))

    # ==================================================
    # 결과 없음
    # ==================================================
    if not top_results:

        send_discord_message(
            "❌ 데이터 없음"
        )

        return

    # ==================================================
    # 디스코드 출력
    # ==================================================
    for stock in top_results:

        message = (

            f"🚨 테스트 종목\n\n"

            f"🔥 종목: "
            f"{stock['name']}\n\n"

            f"💰 거래대금: "
            f"{stock['trading_value']}억\n"

            f"📈 현재가: "
            f"{stock['close']:,}원"

        )

        send_discord_message(
            message
        )

        time.sleep(1)

# ==================================================
# 실행
# ==================================================
if __name__ == "__main__":

    main()